package model;

public class Contactfushi {

    private String f_name;
    private int fs_id;
    private int jg;
    private String fstp;
    private String fsjs;


    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public int getFs_id() {
        return fs_id;
    }

    public void setFs_id(int fs_id) {
        this.fs_id = fs_id;
    }

    public int getJg() {
        return jg;
    }

    public void setJg(int jg) {
        this.jg = jg;
    }

    public String getFstp() {
        return fstp;
    }

    public void setFstp(String fstp) {
        this.fstp = fstp;
    }

    public String getFsjs() {
        return fsjs;
    }

    public void setFsjs(String fsjs) {
        this.fsjs = fsjs;
    }
}
